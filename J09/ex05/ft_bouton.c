int	ft_bouton(int i, int j, int k)
{
	int res;

	res = 0;
	if ((i <= j && j <= k) || (k <= j && j <= i))
		res = j;
	if ((j <= i && i <= k) || (k <= i && i <= j))
		res = i;
	if ((i <= k && k <= j) || (j <= k && k <= i))
		res = k;
	return (res);
}
