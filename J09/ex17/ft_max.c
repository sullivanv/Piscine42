int	ft_max(int *tab, int length)
{
	int i;
	int max;

	max = tab[0];
	i = 0;
	while (i + 1 < length)
	{
		if (max < tab[i + 1])
			max = tab[i + 1];
		i++;
	}
	return (max);
}
